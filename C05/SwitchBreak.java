class SwitchBreak
{
	public static void main(String[] args)
	{
		int n = 2;
		
		switch(n)
		{
		case 1:
			System.out.println("here is 1");
			break;
		case 2:
			System.out.println("here is 2");
			break;
		case 3:
			System.out.println("here is 3");
			break;
		default:
			System.out.println("here is default");
			break;
		}
		System.out.println("finish");	
	}
}